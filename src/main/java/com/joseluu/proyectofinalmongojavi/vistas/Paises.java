    private void btnEliminarPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPaisActionPerformed
        String nombrePais = (String) comboBoxPaises.getSelectedItem();

        if (managerDatabase.runMongoDatabase()) {

            // Buscar paises por nombre
            Pais pais = managerDatabase.getListaPaises()
                    .stream()
                    .filter(p -> p.getNombrePais().equalsIgnoreCase(nombrePais))
                    .findFirst()
                    .orElse(null);

            if (pais != null) {
                    managerDatabase.deletePais(pais);
                    this.inicializarComboBoxPaises();
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró el continente.");
            }

            managerDatabase.closeMongoDatabase();

        } else {
            JOptionPane.showMessageDialog(this, "Error con la database");
        }
    }//GEN-LAST:event_btnEliminarPaisActionPerformed

